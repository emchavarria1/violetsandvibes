const LOCALE_PREFERENCE_KEY = "vv_locale_preference_v1";

const detectLocale = (): SupportedLocale => {
  if (typeof navigator === "undefined") return "en";

  const preferredLocales = navigator.languages?.length
    ? navigator.languages
    : [navigator.language];

  const hasSpanish = preferredLocales.some((locale) =>
    locale?.toLowerCase().startsWith("es"),
  );

  return hasSpanish ? "es" : "en";
};

type I18nContextValue = {
  locale: SupportedLocale;
  localePreference: LocalePreference;
  setLocalePreference: (preference: LocalePreference) => void;
  t: (key: string, vars?: Record<string, string | number | boolean | undefined>) => string;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [localePreference, setLocalePreference] = useState<LocalePreference>(() => {
    if (typeof window === "undefined") return "system";
    const stored = window.localStorage.getItem(LOCALE_PREFERENCE_KEY);
    return stored === "en" || stored === "es" || stored === "system" ? stored : "system";
  });

  useEffect(() => {
    if (typeof window === "undefined") return;
    window.localStorage.setItem(LOCALE_PREFERENCE_KEY, localePreference);
  }, [localePreference]);

  const locale = useMemo<SupportedLocale>(() => {
    return localePreference === "system" ? detectLocale() : localePreference;
  }, [localePreference]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (
      key: string,
      vars?: Record<string, string | number | boolean | undefined>,
    ): string => {
      const translation = translations[locale][key] ?? translations.en[key] ?? key;
      return typeof translation === "function" ? translation(vars) : translation;
    };

    return { locale, localePreference, setLocalePreference, t };
  }, [locale, localePreference]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = (): I18nContextValue => {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used within an I18nProvider");
  }

  return context;
};
